def getDistance(pos,end):
    x,y = pos
    return abs(x-end[0]) + abs(y-end[1])

def addToVisited(var):
    global pathsVisited
    node = pathsVisited
    for x in var:
        if x not in node:
            node[x] = {}
        node = node[x]

def isVisited(var):
    global pathsVisited
    node = pathsVisited
    for x in var:
        if x not in node:
            return False
        node = node[x]
    return True

def enqueue(var):
    x,y,steps = var
    global qualifiedDict,pathsVisited
    distance = getDistance([x,y],end)
    score = steps + distance
    if score not in qualifiedDict:
        qualifiedDict[score] = {}
    if distance not in qualifiedDict[score]:
        qualifiedDict[score][distance] = []
    qualifiedDict[score][distance].append([x,y,steps])
    

def newPop():
    global qualifiedDict
    minScore = min(qualifiedDict.keys())
    minList = qualifiedDict[minScore]
    minSteps = max(minList.keys())
    minStep = minList[minSteps]
    returnVal = minStep.pop(0)
    if len(minStep) == 0:
        del qualifiedDict[minScore][minSteps]
        if len(qualifiedDict[minScore]) == 0:
            del qualifiedDict[minScore]
    return returnVal

