def walk(x,y):
    original = (x,y)
    paths={}
    neighs = getNeighs(x,y)
    for neigh in neighs:
        if readNeigh(neigh)=="▒": continue
        xx,yy = neigh
        path=[neigh]
        breakFlag = False
        while True:
            for neigh in getNeighs(xx,yy):
                xx,yy = neigh
                if any([ neigh in path, neigh == original, readNeigh(neigh)=="▒" ]): continue
                if isJunction(xx,yy) or readNeigh(neigh).isalpha():
                    path.append(neigh)
                    breakFlag=True
                    break
                if  isHall(xx,yy): 
                    path.append(neigh)
                    xx,yy=neigh
                    break
            if breakFlag: break
        paths[neigh] = len(path)
    return paths

def bfs():
    k,l=start
    tree={}
    queue=[(k-1,l-1)]
    while queue:
        node = queue.pop(0)
        x,y=node
        if readNeigh(node).isalpha(): node=readNeigh(node)
        if node not in tree: tree[node] = {}
        dic=walk(x,y)
        for dest,dist in dic.items():
            char = readNeigh(dest)
            if not char.isalpha():char=dest
            if char not in tree:
                tree[char] = {}
                queue.append(dest)
            tree[char][node] = dist
            tree[node][char] = dist
    return tree


